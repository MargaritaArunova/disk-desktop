package com.diskdesktop.model;

public class DirectoryInfo {

    private String name;
    private String path;

    public DirectoryInfo() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}

